package WVToolAdditions;

import WVToolExtension.JPWVTDocumentInfo;
import Algorithms.Algorithm;
import edu.udo.cs.wvtool.main.WVTFileInputList;

public class JPLoadData {

	private WVTFileInputList fileInputList;
	private JPWVTDocumentInfo[] documents;
	private JPWVTDocumentInfo mainDocument;
	private Algorithm algorithm;
	
	public JPLoadData(WVTFileInputList fileInputList,
			JPWVTDocumentInfo mainDocument, JPWVTDocumentInfo[] documents, Algorithm algorithm) {
		this.fileInputList = fileInputList;
		this.mainDocument = mainDocument;
		this.documents = documents;
		this.algorithm = algorithm;
	}

	public WVTFileInputList getFileInputList() {
		return fileInputList;
	}

	public void setFileInputList(WVTFileInputList fileInputList) {
		this.fileInputList = fileInputList;
	}

	public JPWVTDocumentInfo[] getDocuments() {
		return documents;
	}

	public void setDocuments(JPWVTDocumentInfo[] documents) {
		this.documents = documents;
	}

	public JPWVTDocumentInfo getMainDocument() {
		return mainDocument;
	}

	public void setMainDocument(JPWVTDocumentInfo mainDocument) {
		this.mainDocument = mainDocument;
	}
	
	public Algorithm getAlgorithm() {
		return algorithm;
	}

	public void setAlgorithm(Algorithm algorithm) {
		this.algorithm = algorithm;
	}
}
